public class Swapping {     // create class
	 public static void main(String[] args) {    // main
	        // local variable
		   int t;
		   int a=69;
		   int b=96;

		   System.out.println("Before swapping : a, b = "+a+","+ b);
                       //swapping 2 numbers
		   t=a;               
		   a=b;           
		   b=t;   

		  System.out.println("After swapping : a, b = "+a+","+ b);   //print swapped number
     }
}
